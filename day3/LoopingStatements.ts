// while loop
// loop until the condition is true
let i: number = 0;
while (i < 5) {
    console.log(i);
    i++;
} // output: 0 1 2 3 4

// do while loop
// loop at least once and then check the condition
let j: number = 0;
do {
    console.log(j);
    j++;
} while (j < 5); // output: 0 1 2 3 4

// for loop
// loop with initialization, condition and increment/decrement
for (let k: number = 0; k < 5; k++) {
    console.log(k);
} // output: 0 1 2 3 4
